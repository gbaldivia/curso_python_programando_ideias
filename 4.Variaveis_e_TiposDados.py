"""
O que são variáveis?
- Modo de rotular ou nomear os dados pertencentes ao programa

O que são dados?
- Todas as informações que serão utilizadas ao longo do código, podendo ser
    números, palavras, frases, textos, entre outros.

Existem 2 tipos de variáveis:

1 - Global: variáveis que podem ser manipuladas ao longo de todo o código
2 - Local: Manipulada apelas dentro de uma determinada parte do programa

Como declarar variáveis?

nome = dado

Tipos de Dados:
1 - Inteiros: números sem casas decimais.
2 - Flutuantes: números que possuem casas decimais.
3 - Complexo: conjunto dos números complexos. Exemplo: 1 + 2j
4 - Booleana: 1 = True e 0 = False
5 - String: todos os dados entre aspas simples, aspas duplas ou aspas simples triplas

Para inverter o conteúdo presente na string, basta adicionar [::-1]
var_string = 'Tom'
print(ar_string[::-1])
"""

